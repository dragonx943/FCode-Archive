    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        int choice = JOptionPane.showConfirmDialog(this, "Ban co muon dang ky khong?", "Xac nhan", JOptionPane.YES_NO_CANCEL_OPTION);
        if(JOptionPane.YES_OPTION == choice){
            String a = txtMa.getText();
            String b = txtTen.getText(); 
            String c = String.valueOf(cboThuongHieu.getSelectedItem());
            Double d = Double.parseDouble(txtGia.getText());
            int e = Integer.parseInt(txtSoLuong.getText());
            int f = 0;
            if (rdoMo.isSelected()) {
                f = 1;
            } else if (rdoNgung.isSelected()) {
                f = 0;
            }
            
            service.them(a, b, c, d, e, f);
            loadTable();
        
            JOptionPane.showMessageDialog(this,"Dang ky thanh cong","Thong bao",JOptionPane.INFORMATION_MESSAGE);
        } else if(JOptionPane.NO_OPTION == choice) {
            JOptionPane.showMessageDialog(this,"Dang ky that bai","Thong bao",JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this,"Ban da huy dang ky","Thong bao",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int rowIndex = tableList.getSelectedRow();
        if (rowIndex != -1) {
            service.deletePick(rowIndex);
            loadTable();
            JOptionPane.showMessageDialog(this, "Đã xoá dữ liệu thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần xoá", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int rowIndex = tableList.getSelectedRow();
        if (rowIndex != -1) {
            String a = txtMa.getText();
            String b = txtTen.getText(); 
            String c = String.valueOf(cboThuongHieu.getSelectedItem());
            Double d = Double.parseDouble(txtGia.getText());
            int e = Integer.parseInt(txtSoLuong.getText());
            int f = 0;
            if (rdoMo.isSelected()) {
                f = 1;
            } else if (rdoNgung.isSelected()) {
                f = 0;
            }
            service.updatePick(rowIndex, a, b, c, d, e, f);
            loadTable();
            JOptionPane.showMessageDialog(this, "Đã sửa dữ liệu thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng cần sửa", "Cảnh báo", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed


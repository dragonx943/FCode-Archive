    public void loadTable() {
        _newTable = (DefaultTableModel) tableList.getModel();
        _newTable.setRowCount(0);
        
        for (Pickleball pickle : list) {
            _newTable.addRow(new Object[]{
                pickle.getMa(),
                pickle.getTen(),
                pickle.getThuongHieu(),
                pickle.getGia(),
                pickle.getSoLuong(),
                pickle.checkStatus(),
            });
        }
    }
    
    public void loadData(int index) {
        Pickleball first = list.get(index);
        txtTen.setText(first.getTen());
        txtMa.setText(first.getMa());
//        txtThuongHieu.setText(first.getThuongHieu());
        if (first.getThuongHieu().equalsIgnoreCase("Wilson")) {
            cboThuongHieu.setSelectedItem("Wilson");
        } else cboThuongHieu.setSelectedItem("Joola");
        txtGia.setText(String.valueOf(first.getGia()));
        txtSoLuong.setText(String.valueOf(first.getSoLuong()));
        if (first.getTrangThai() == 1) {
            rdoMo.setSelected(true);
        } else rdoNgung.setSelected(true);
    }

